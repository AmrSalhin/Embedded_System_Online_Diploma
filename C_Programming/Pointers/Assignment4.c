#include <stdio.h>
void main(){
	int arr[15];
	int x;
	int *p;
	p=arr;
	printf("Enter the number of elements in the array : ");
	scanf("%d",&x);
	printf("Enter the array elements : ");
	for(int i=0;i<x;i++){
		scanf("%d",p);
		p++;
	}
	p--;
	printf("The elements of the array in reversed order : ");
	for(int i=0;i<x;i++){
		printf("%d",*p);
		p--;
	}
}