#include <stdio.h>
void main(void){
	char str[50];
	char* p;
	int x=1;
	p=str;
	gets(str);
	while(1){
		if(*p=='\0'){
			break;
		}else{
			p++;
			x++;
		}
	}
	printf("The Original text : %s\n",str);
	printf("The Reversed text : ");
	for(int i=0;i<x;i++){
		printf("%c",*p);
		p--;
	}
}